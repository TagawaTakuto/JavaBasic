public class PTra02_01 {
	public static void main(String[] args) {

		int a;

		a = 20 / 5;

	System.out.println(a);	// ★ 20を5で割った値を出力してください


		int b;

		b = 126 * 6;
	System.out.println(b); // ★ 126に13を掛けた値を出力してください


		int c;

		c = 97 % 6;

	System.out.println(c); // ★ 97を6で割ったときの余りを出力してください


	}
}
